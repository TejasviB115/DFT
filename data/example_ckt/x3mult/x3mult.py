def x3mult_sim(in_vec):
    #in_vec = {'1':'0', '2':'0', '3':'0'}
    #out_vec = {'38':'0', '39':'0', '40':'0'}
    sim = {'000':'000', '001':'100', '010':'001', '011':'000', 
            '100':'101', '101':'010', '110':'110', '111':'101'}  
    PI = ''
    for i in in_vec.values():
        PI += str(i)

    PO = sim[PI]
    out_vec = {'38':PO[0], '39':PO[1], '40':PO[2]}

    return out_vec




